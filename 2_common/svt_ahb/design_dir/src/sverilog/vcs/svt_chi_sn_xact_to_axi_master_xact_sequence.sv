`ifndef GUARD_SVT_CHI_SN_XACT_TO_AXI_MASTER_XACT_SEQUENCE_SV
`define GUARD_SVT_CHI_SN_XACT_TO_AXI_MASTER_XACT_SEQUENCE_SV

/** @cond PRIVATE */
// =============================================================================
/**
 * This sequence creates a reporter reference
 */
class svt_chi_sn_xact_to_axi_master_xact_sequence extends svt_sequence#(svt_chi_sn_transaction);

  /*  Response request from the SN sequencer */
  svt_chi_sn_transaction req_resp;

  /** 
   * Factory Registration. 
   */
  `svt_xvm_object_utils(svt_chi_sn_xact_to_axi_master_xact_sequence)

  /** 
   * Parent Sequencer Declaration. 
   */
  `svt_xvm_declare_p_sequencer(svt_chi_sn_transaction_sequencer)

  /** Handle for axi_master_sequencer */
  svt_axi_master_sequencer axi_master_sequencer;

  // ****************************************************************************
  // Local Data Properties
  // ****************************************************************************
  
  /** @cond PRIVATE */
  /** AXI Port Configuration obtained from the AXI Master sequencer. */
  local svt_axi_port_configuration axi_master_cfg;
  
  /** CHI Node configuration obtained from the CHI sn virtual sequencer. */
  local svt_chi_node_configuration chi_node_cfg;
  /** @endcond */

  // ****************************************************************************
  // Public Methods
  // ****************************************************************************
  /** 
   * Constructs a new svt_chi_sn_xact_to_axi_master_xact_sequence instance.
   * 
   * @param name Sequence instance name.
   */
  extern function new(string name="svt_chi_sn_xact_to_axi_master_xact_sequence");

  /** Function that disables randomization for specific fields of AXI Master
    * transaction which will have a direct mapping to corresponding fields in
    * CHI SN transaction  */
  extern function void set_axi_rand_mode(svt_axi_master_transaction axi_xact);  

  /** Maps CHI transaction to the corresponding AXI transaction.
   * The mapped AXI transaction is executed on the AXI master sequencer.
   * CHI response is suspended till the transaction completes at AXI end and
   * the corresponding response recieved.
   * Once the AXI transaction completes the AXI response is mapped to
   * corresponding CHI response and the response mechanism is resumed.
   */
  extern task map_chi_xact_to_axi_xact(svt_chi_sn_transaction chi_xact,svt_axi_master_transaction axi_xact);
  
  /** Function that maps CHI Mem attributes to AXI cache types */
  extern function void map_chi_mem_attr_to_axi_cache_type(svt_chi_transaction chi_xact,svt_axi_master_transaction axi_xact,bit is_axi3_slave = 0,bit is_ace_lite_slave = 0,bit is_axi4_slave = 0);
  
  /** Function that maps CHI data size to corresponding AXI burst size */
  extern function void map_chi_data_size_to_axi_burst_size(svt_chi_transaction chi_xact,svt_axi_master_transaction axi_xact,bit is_narrow_transfer = 0,bit configure_burst_size_using_data_width = 0);
  
  /** Function that maps CHI xact type to corresponding AXI xact type */
  extern function void map_chi_xact_type_to_axi_xact_type(svt_chi_transaction chi_xact,svt_axi_master_transaction axi_xact,bit is_axi3_slave = 0,bit is_ace_lite_slave = 0,bit is_axi4_slave = 0);
  
  /** Function that waits for AXI transaction to end */
  extern task wait_for_axi_xact_to_end(svt_axi_master_transaction add_to_axi_active_xact[$]);
  
  /** Function that maps AXI Read data to CHI data */
  extern function void map_axi_read_data_to_chi_data(svt_chi_transaction chi_xact,svt_axi_master_transaction axi_xact);

  /** Function that maps AXI response to corresponding CHI response type */
  extern function void map_axi_resp_to_chi_resp(svt_chi_transaction chi_xact,svt_axi_master_transaction axi_xact);
  
  /**
   * Wait for a response request
   */
  extern task wait_for_response_request(output svt_chi_sn_transaction req);

  /** Used to sink the responses from the response queue */
  extern virtual task sink_responses();

  /** Used to set the Burst Length and Size of AXI transaction based on the number of bytes to be transferred */
  extern function void set_axi_burst_length_size(svt_axi_master_transaction axi_xact, int bytes_in_xact);
  
  /** Body of the sequence */ 
  extern virtual task body();

endclass: svt_chi_sn_xact_to_axi_master_xact_sequence
