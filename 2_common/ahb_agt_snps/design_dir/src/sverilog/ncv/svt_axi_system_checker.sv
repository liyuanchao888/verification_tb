    */
  svt_err_check_stats burst_size_match_for_non_modifiable_xact_check;

  /**
    * For non-modifiable transactions with ARCACHE[1] == 1'b0 or AWCACHE[1] ==
    * 1'b0, the cache_type should match between master transaction and
    * corresponding slave transaction, except for ARCACHE[0]/AWCACHE[0].
