    */
  svt_err_check_stats interconnect_dvm_complete_issue_check;

  /**
    * Checks that the interconnect sets RRESP value of a DVM transaction
    * correctly based on the following:
    * The interconnect component gathers all response values and responds to the
    * originator as follows:
    * if CRRESP is 0b00000 for all responses, the interconnect component sets
    * RRESP to 0b0000. If CRRESP is 0b00010 for any responses, the interconnect
    * component sets RRESP to 0b0010.
    */
  svt_err_check_stats interconnect_dvm_response_value_check;

  /** 
