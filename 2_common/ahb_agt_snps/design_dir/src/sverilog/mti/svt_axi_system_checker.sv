  /**
    * Checks that the effective number of bytes for write transactions sent by
    * master components match that which are sent by the interconnect to the
    * slaves. This basically checks for spurious writes from the interconnect.
    * The effective write byte count is the number of bytes for which write
    * strobe is asserted. If there are writes generated by the interconnect
    * because dirty data returned by a snoop could not be passed back to a
    * master, the effective write byte count includes the number of bytes in a
    * snoop also. 
    */
  svt_err_check_stats write_byte_count_match_across_interconnect;
  
  /**
    * Checks that cache maintenance transactions are forwarded to slaves
