class svt_axi_system_checker extends svt_err_check;

  local svt_axi_system_configuration cfg;

  //local svt_axi_port_configuration port_cfg;

  local string group_name = "";

  local string sub_group_name = "";

  /** Enables protocol check coverage provided it protocol_checks_coverage_enable is set
    * in the port configuration as well. If enable_pc_cov is 0, then protocol checks coverage
    * will not be enabled, even if it is set in configuration
    */
  local bit enable_pc_cov = 1;


  //--------------------------------------------------------------
  /** Checks that the address of a snoop transaction matches address of one of the outstanding coherent transactions. 
    * The VIP requires that svt_axi_port_configuration::cache_line_size of ACE_LITE port type matches that of AXI_ACE port types. 
    * If cache line size of ACE_LITE and AXI_ACE ports does not match, this check would misfire.
    */
    
  svt_err_check_stats snoop_addr_matches_coherent_addr_check;

  /** Checks that the snoop transaction type corresponds to the coherent transaction type of initiating master */
  svt_err_check_stats coherent_snoop_type_match_check;

  /** Checks that the port on which snoop transaction is received corresponds to the domain indicated in coherent transaction of initiating master */
  svt_err_check_stats coherent_snoop_domain_match_check;

  /**
    * Monitor Check that the security level (determined by signal ACPROT[1]) of
    * the snoop transaction matches the security level of corresponding
    * coherent transaction (determined by signal AxPROT[1])
    */
  svt_err_check_stats coherent_snoop_prot_type_match_check;

  /** Checks that a snoop is not sent to the initiating master */ 
  svt_err_check_stats snoop_not_sent_to_initiating_master_check;

  /** Checks that READNOSNOOP,WRITENOSNOOP,WRITEBACK,WRITECLEAN and EVICT do not cause a snoop of cached masters */
  svt_err_check_stats coherent_xact_with_no_snoop_check;

  /** Checks that the response to a coherent transaction is not started before sufficient information from snooped masters are obtained */
  svt_err_check_stats coherent_resp_start_conditions_check;

  /** Checks that the IsShared response to initiating master is correct */
  svt_err_check_stats coherent_resp_isshared_check;

  /** Checks that the PassDirty response to initiating master is correct */
  svt_err_check_stats coherent_resp_passdirty_check;

  /** Checks that no two responses to a snoop transaction have the WasUnique(CRRESP[4]) bit asserted */
  svt_err_check_stats snoop_resp_wasunique_check;

  /** Checks that no two responses to a snoop transaction have the PassDirty(CRRESP[2]) bit asserted */
  svt_err_check_stats snoop_resp_passdirty_check;

  /** Checks that data returned from all snoop transactions are consistent */
  svt_err_check_stats snoop_data_consistency_check;

  /** Checks that data returned to initiating master matches the data received from snoop transaction */
  svt_err_check_stats coherent_and_snoop_data_match_check;

  /** Checks that if two masters access the same cache line, one master is sequenced after the other */
  svt_err_check_stats overlapping_addr_sequencing_check;

  /** 
    * Checks that no two masters have the same cacheline in unique state 
    * This check is not performed if the agents connected to the interfaces
    * are configured to work in passive mode.
    */
  svt_err_check_stats no_two_cachelines_in_unique_state_check;

  /** 
    * Checks that no two masters have the same cacheline in dirty state 
    * This check is not performed if the agents connected to the interfaces
    * are configured to work in passive mode.
    */
  svt_err_check_stats no_two_cachelines_in_dirty_state_check;

  /** 
    * Checks that if all cachelines are clean, the data in cacheline is consistent
    * with data in memory
    * This check is not performed if the agents connected to the interfaces
    * are configured to work in passive mode.
    */
  svt_err_check_stats cacheline_and_memory_coherency_check;

  /** 
    * Checks that the interconnect generated slave write transaction to update 
    * main memory when:
    * 1) A snoop response has the PassDirty response asserted, and the interconnect
    *   does not assert the PassDirty transaction response for the initiating master 
    * 2) The interconnect has provided a read response to an initiating master before 
    *   it has received all the snoop responses and a later snoop response has PassDirty asserted
    *   
    * This check reports an ERROR at the end of simulation, if the interconnect fails to 
    * generate slave write transaction under the above described situations. 
    *
    * This check passes when the system monitor associates interconnect generated slave write 
    * transaction to the corresponding snoop transaction that returned dirty data. 
    *
    * The master_slave_xact_data_integrity_check must be enabled for this check to function 
    * correctly. 
    *
    * svt_axi_system_configuration::interconnect_generated_write_xact_to_update_main_memory_check_enable
    * must to set to 1 to enable this check.
    * 
    */
  svt_err_check_stats interconnect_generated_write_xact_to_update_main_memory_check;

  /**
    * Checks that if all cachelines are clean, the data in cacheline is
    * consistent with data in memory. This check is done on a per
    * transaction basis at the end of each coherent transaction.
    * Note that in some implementations a transaction to the memory
    * could still be in the interconnect's buffer in which case this
    * check would fail. Hence this check is issued as a warning, rather
    * than an error.
    * This check is not performed if the agents connected to the interfaces
    * are configured to work in passive mode.
    */
  svt_err_check_stats cacheline_and_memory_coherency_check_per_xact;

  /**
    * Checks that a transaction is routed correctly to a slave port
    * based on address
    */
  svt_err_check_stats slave_transaction_routing_check;

  /**
    * Applicable only when svt_axi_system_configuration::posted_write_xacts_enable
    * is not set.
    * Checks that data in transaction is consistent with data in memory when the
    * transaction completes. This checks that a WRITE transaction issued by a
    * master is written to memory correctly. Similarly, it checks that a READ
    * transaction fetches the correct data from memory.  The check assumes that
    * a transaction issued by a master completes only after response is received
    * from the slave to which that transaction was routed. It also assums that
    * there is no other transaction that accesses an overlapping address during
    * the period that the response is issed by the slave and the transaction
    * completes in the master that issued the transaction.  In ACE, this check
    * is issued only when the snoop has not returned any data and data is
    * fetched from memory or when data is written to memory. 
    */
  svt_err_check_stats data_integrity_check;

  /**
    * Applicable only when svt_axi_system_configuration::posted_write_xacts_enable
    * is not set.
    * Checks that the read data returned for a coherent read is the same as
    * that of a WRITEBACK or WRITECLEAN transaction which was in progress when
    * the read was initiated. This is to check that an interconnect that does
    * speculative reads re-fetches the data in a scenario where the preceding
    * WRITEBACK/WRITECLEAN did not complete and none of the snoops returned
    * data. For example, consider that a WRITEBACK was in progress while a read
    * is initiated by the master. If the interconnect does speculative reads,
    * the speculative read may return stale data since the WRITEBACK is not
    * complete.  If none of the snoops return data as well, the interconnect
    * should re-fetch data after the completion of the WRITEBACK.
    */
  svt_err_check_stats data_integrity_with_outstanding_coherent_write_check;

   /**
    * Checks that the data in a slave transaction is the same as that of the
    * corresponding master transaction. In order to perform this check, slave
    * transactions are correlated to corresponding master transactions.  This
    * check is done only when either of 
    * svt_axi_system_configuration::master_slave_non_modifiable_xact_checks_enable,
    * svt_axi_system_configuration::posted_write_xacts_enable or svt_axi_system_configuration::master_slave_xact_data_integrity_check_enable is set.
    * When only the former is set, this check is done only on non-modifiable
    * transactions. Note that
    * posted_write_xacts_enable can be set only when there are no
    * svt_axi_port_configuration::AXI_ACE ports in the system.
    * svt_axi_system_configuration::master_slave_xact_data_integrity_check_enable may be set in systems with ACE components.
    */
  svt_err_check_stats master_slave_xact_data_integrity_check;

  /**
    * Applicable only if svt_axi_system_configuration::posted_write_xacts_enable is
    * set.
    * Checks that a READ transaction is routed to a slave only after a previous
    * WRITE transaction from the same port and with an overlapping address 
    * completes at the slave. This is required in posted writes to ensure that a
    * READ transaction retreives the same data as the previous WRITE transaction.
    */
  svt_err_check_stats slave_read_xact_timing_relative_to_last_posted_write_xact_check;

