    */
  svt_err_check_stats burst_type_match_for_non_modifiable_xact_check;

  /**
    * For non-modifiable transactions with ARCACHE[1] == 1'b0 or AWCACHE[1] ==
    * 1'b0, the burst_size should match between master transaction and
    * corresponding slave transaction
    * Enabled only when
    * svt_axi_system_configuration::master_slave_non_modifiable_xact_checks_enable
    * is set in system configuration
    */
  svt_err_check_stats burst_size_match_for_non_modifiable_xact_check;

  /**
    * For non-modifiable transactions with ARCACHE[1] == 1'b0 or AWCACHE[1] ==
    * 1'b0, the cache_type should match between master transaction and
    * corresponding slave transaction, except for ARCACHE[0]/AWCACHE[0].
    * Enabled only when
    * svt_axi_system_configuration::master_slave_non_modifiable_xact_checks_enable
    * is set in system configuration
    */
  svt_err_check_stats cache_type_match_for_non_modifiable_xact_check;

  /**
    * For non-modifiable transactions with ARCACHE[1] == 1'b0 or AWCACHE[1] ==
    * 1'b0, transactions in the same direction to the same slave with the same
    * ID, must be ordered irrespective of the address
    * Enabled only when
    * svt_axi_system_configuration::master_slave_non_modifiable_xact_checks_enable
    * is set in system configuration
    */
  svt_err_check_stats ordering_for_non_modifiable_xact_check;

  /**
    * For device non-bufferable transactions, responses must be obtained from
    * the final destination. Checks that the timing of the response of the
    * master transaction is after that of the corresponding slave transaction
    * and that the responses match.
    * Enabled only when
    * svt_axi_system_configuration::master_slave_non_modifiable_xact_checks_enable
    * is set in system configuration
    */
  svt_err_check_stats device_non_bufferable_response_match_check;
  
  /**
    * At the end of simulation there should be no non-modifiable transaction
    * that hasn't been mapped to a slave transaction. If such a transaction exists
    * it shows that a non-modifiable transaction hasn't been made visible at the
    * slave, which is not allowed
    * Enabled only when
    * svt_axi_system_configuration::master_slave_non_modifiable_xact_checks_enable
    * is set in system configuration
    */
  svt_err_check_stats eos_unmapped_non_modifiable_xact;

  /**
    * Applicable only when svt_axi_system_configuration::posted_write_xacts_enable
    * is set. 
    * At the end of the simulation if there are master transactions that could not
    * be associated to corresponding slave transactions, this error is issued.
    */
  svt_err_check_stats eos_unmapped_master_xact;

  /**
    * Checks that an exclusive sequence is reset after a cacheline is
    * invalidated by a snoop. This checks that after a snoop invalidates
    * a cacheline, an exclusive load is always sent prior to sending the
    * exclusive store. 
    */
  svt_err_check_stats restart_exclusive_seq_post_cache_line_invalidation_check;

  /** Checks that if cacheline is in invalid state then exclusive load transaction is issued
    * only as READCLEAN or READSHARED 
    */
  svt_err_check_stats exclusive_load_from_valid_state_sys_check;

  /** Checks that if cacheline is in invalid state then exclusive store transaction is not issued
    */
  svt_err_check_stats exclusive_store_from_valid_state_sys_check;

  /** Checks if interconnect stopped snoop propagation for exclusive transaction that was not successful */
  svt_err_check_stats exclusive_snoop_propagation_check;

  /**
    * Checks that the effective number of bytes for write transactions sent by
    * master components match that which are sent by the interconnect to the
    * slaves. This basically checks for spurious writes from the interconnect.
    * The effective write byte count is the number of bytes for which write
    * strobe is asserted. If there are writes generated by the interconnect
    * because dirty data returned by a snoop could not be passed back to a
    * master, the effective write byte count includes the number of bytes in a
    * snoop also. 
    */
  svt_err_check_stats write_byte_count_match_across_interconnect;
  
  /**
    * Checks that cache maintenance transactions are forwarded to slaves
    * If two or more slaves are in the same address space of the memory
    * map, this check is performed on each of the slave ports with 
    * overlapping address.
    * The check is performed only if forward_cmos_to_slaves_check_enable is
    * set in the port configuration of the slave
    */
  svt_err_check_stats forward_cmos_to_slave_check;


