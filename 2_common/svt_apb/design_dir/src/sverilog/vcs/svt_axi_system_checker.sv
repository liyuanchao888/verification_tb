    */
  svt_err_check_stats prot_type_match_for_non_modifiable_xact_check;

  /**
    * For non-modifiable transactions with ARCACHE[1] == 1'b0 or AWCACHE[1] ==
    * 1'b0, the region should match between master transaction and
    * corresponding slave transaction
    * Enabled only when
    * svt_axi_system_configuration::master_slave_non_modifiable_xact_checks_enable
    * is set in system configuration
    */
  svt_err_check_stats region_match_for_non_modifiable_xact_check;

  /**
    * For non-modifiable transactions with ARCACHE[1] == 1'b0 or AWCACHE[1] ==
    * 1'b0, the burst_length should match between master transaction and
    * corresponding slave transaction if burst_length <= 16. For larger burst
    * lengths a transaction could get split if getting routed to an AXI
    * interface that doesn't support burst length > 16. This check is not done
    * when burst_length > 16.
    * Enabled only when
    * svt_axi_system_configuration::master_slave_non_modifiable_xact_checks_enable
    * is set in system configuration
    */
  svt_err_check_stats burst_length_match_for_non_modifiable_xact_check;

  /**
    * For non-modifiable transactions with ARCACHE[1] == 1'b0 or AWCACHE[1] ==
    * 1'b0, the burst_type should match between master transaction and
